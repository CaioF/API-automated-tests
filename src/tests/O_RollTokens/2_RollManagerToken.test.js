'use strict'

const hippie = require('hippie');
const tokens = require('../../tokens.json');
const config = require('../../config.json');
const createdIDs = require('../../createdIDs.json');

function api() {
  return hippie()
    .json()
    .base(config.url)
    .header('Authorization', tokens.managerToken)
    .del(`/RollManagerToken`)
}

describe('DEL /RollManagerToken\nDeletes all previous authorization tokens of a Manager by ID', () => {

  it('check to see if the Manager has a valid token', (done) => {
    hippie()
    .json()
    .base(config.url)
    .header('Authorization', tokens.managerToken2)
    .get(`/GetEmployeeGroup?ID=${createdIDs.employeeGroup}`)
    .expectStatus(200)
    .end( (err, res, body) =>
    {  
      if (err) {
        throw new Error(`\nMOCHA ERR:\n${err.message}\n\nRESPONSE ERR:\n${JSON.stringify(body)}`)
      } else {
        done()
      }
    })
  });

  it('returns 200 when the specified Manager ID is in the DB and is logged in', (done) => {
    api()
    .send({
      "ID": createdIDs.manager
    })
    .expectStatus(200)
    .end( (err, res, body) =>
    {  
      if (err) {
        throw new Error(`\nMOCHA ERR:\n${err.message}\n\nRESPONSE ERR:\n${JSON.stringify(body)}`)
      } else {
          done()
      }
    })
  });

  it('check to see if the Manager token is no longer valid', (done) => {
    hippie()
    .json()
    .base(config.url)
    .header('Authorization', tokens.managerToken2)
    .get(`/GetEmployeeGroup?ID=${createdIDs.employeeGroup}`)
    .expectStatus(401)
    .end( (err, res, body) =>
    {  
      if (err) {
        throw new Error(`\nMOCHA ERR:\n${err.message}\n\nRESPONSE ERR:\n${JSON.stringify(body)}`)
      } else {
        done()
      }
    })
  });

  it('returns 404 when the specified Manager ID is not in the DB', (done) => {
    api()
    .send({
      "ID": createdIDs.manager+99
    })
    .expectStatus(404)
    .expectValue('code', 5)
    .end( (err, res, body) =>
    {  
      if (err) {
        throw new Error(`\nMOCHA ERR:\n${err.message}\n\nRESPONSE ERR:\n${JSON.stringify(body)}`)
      } else {
          done()
      }
    })
  });

  it('returns 400 when the request body params do not match the specification', (done) => {
    api()
    .send({
      "ID": 'a'
    })
    .expectStatus(400)
    .expectValue('code', 3)
    .end( (err, res, body) =>
    {  
      if (err) {
        throw new Error(`\nMOCHA ERR:\n${err.message}\n\nRESPONSE ERR:\n${JSON.stringify(body)}`)
      } else {
          done()
      }
    })
  });

  it('returns 404 when the request body params are null', (done) => {
    api()
    .send({
      "ID": null
    })
    .expectStatus(404)
    .expectValue('code', 5)
    .end( (err, res, body) =>
    {  
      if (err) {
        throw new Error(`\nMOCHA ERR:\n${err.message}\n\nRESPONSE ERR:\n${JSON.stringify(body)}`)
      } else {
          done()
      }
    })
  })

})